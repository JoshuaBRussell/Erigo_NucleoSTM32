  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  //Grab NeuroModulation amplitude at startup.
  HAL_ADC_Start(&hadc1);
